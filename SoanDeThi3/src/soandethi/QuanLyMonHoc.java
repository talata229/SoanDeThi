    private void listDSMonHocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDSMonHocMouseClicked
        // TODO add your handling code here:
        if (listDSMonHoc.getSelectedIndex() != -1) {
            txtTenMonHoc.setText(dsmh.get(listDSMonHoc.getSelectedIndex()).getTenMonHoc());
            txtTenMonHoc.setForeground(Color.red);
            txtMaHocPhan.setText(dsmh.get(listDSMonHoc.getSelectedIndex()).getMaMonHoc());
            txtMaHocPhan.setForeground(Color.red);
            spSoChuong.setValue(dsmh.get(listDSMonHoc.getSelectedIndex()).getSoChuong());
            txtGioiThieu.setText(dsmh.get(listDSMonHoc.getSelectedIndex()).getGioiThieu());
            txtGioiThieu.setForeground(Color.red);
        }
    }//GEN-LAST:event_listDSMonHocMouseClicked

