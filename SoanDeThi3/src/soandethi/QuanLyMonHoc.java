    private void btnXoaMonHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMonHocActionPerformed
        // TODO add your handling code here:
        if (listDSMonHoc.getSelectedIndex() != -1) {
            int index = listDSMonHoc.getSelectedIndex();
            dlDSMonHoc.remove(listDSMonHoc.getSelectedIndex());
            //System.out.println(index);
            dsmh.remove(index);
            txtTenMonHoc.setText("Bạn hãy nhập tên môn học");
            txtTenMonHoc.setForeground(Color.gray);
            txtMaHocPhan.setText("Bạn hãy nhập mã học phần");
            txtMaHocPhan.setForeground(Color.gray);
            spSoChuong.setValue(1);
            txtGioiThieu.setText("");
            if (dlDSMonHoc.getSize() == 0) {
                btnXoaMonHoc.setEnabled(false);
                btnXoaMonHoc.setToolTipText("Không có môn học nào trong danh sách");
            }
        }
    }//GEN-LAST:event_btnXoaMonHocActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        MonHoc mh = new MonHoc(txtMaHocPhan.getText().toString(), txtTenMonHoc.getText().toString(), Integer.parseInt(spSoChuong.getValue().toString()), txtGioiThieu.getText().toString());
        int viTriMonHocDuocChon = listDSMonHoc.getSelectedIndex();
        dsmh.remove(viTriMonHocDuocChon);
        dsmh.add(viTriMonHocDuocChon, mh);
        
        dlDSMonHoc.remove(viTriMonHocDuocChon);
        dlDSMonHoc.add(viTriMonHocDuocChon, txtTenMonHoc.getText());
        
    }//GEN-LAST:event_btnSuaActionPerformed

