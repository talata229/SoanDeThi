    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (rdTuLuan.isSelected()) {
            TuLuan tl = new TuLuan(txtDeBai.getText().toString(),
                    Integer.parseInt(cbChuong.getItemAt(cbChuong.getSelectedIndex())),
                    Integer.parseInt(cbDoKho.getItemAt(cbDoKho.getSelectedIndex())),
                    txtGoiYDapAnTuLuan.getText().toString());

            int viTriCauHoiTuLuanDuocChon = listCauHoiTuLuan.getSelectedIndex();

            dsCauHoiTuLuan.remove(viTriCauHoiTuLuanDuocChon);
            dsCauHoiTuLuan.add(viTriCauHoiTuLuanDuocChon, tl);

            dlCauHoiTuLuan.remove(viTriCauHoiTuLuanDuocChon);
            dlCauHoiTuLuan.add(viTriCauHoiTuLuanDuocChon, txtDeBai.getText());

        }
    }//GEN-LAST:event_btnSuaActionPerformed

