    private void btnXoaCauHoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaCauHoiActionPerformed
        // TODO add your handling code here:
        if (listCauHoiTuLuan.getSelectedIndex() != -1 && rdTuLuan.isSelected()) {
            int index = listCauHoiTuLuan.getSelectedIndex();
            dlCauHoiTuLuan.remove(listCauHoiTuLuan.getSelectedIndex());
            //System.out.println(index);
            dsCauHoiTuLuan.remove(index);
            txtDeBai.setText("Cho a =2, b=6. Tìm giá trị của x biết x=a+b.");
            txtDeBai.setForeground(Color.gray);
            txtGoiYDapAnTuLuan.setText("");
            cbChuong.setSelectedIndex(0);
            cbDoKho.setSelectedIndex(0);

        }

        if (listCauHoiTracNghiem.getSelectedIndex() != -1 && rdTracNghiem.isSelected()) {
            int index = listCauHoiTracNghiem.getSelectedIndex();
            dlCauHoiTracNghiem.remove(listCauHoiTracNghiem.getSelectedIndex());
            //System.out.println(index);
            dsCauHoiTracNghiem.remove(index);
           // txtDeBai.setText("Cho a =2, b=6. Tìm giá trị của x biết x=a+b.");
            txtDeBai.setForeground(Color.gray);
            //txtGoiYDapAnTuLuan.setText("");
            cbChuong.setSelectedIndex(0);
            cbDoKho.setSelectedIndex(0);
            dlDapAnTracNghiem.removeAllElements();

        }
        if (dlCauHoiTracNghiem.getSize() == 0 && dlCauHoiTuLuan.getSize() == 0) {
            btnXoaCauHoi.setEnabled(false);
            btnXoaCauHoi.setToolTipText("Không có câu hỏi nào trong danh sách");
        }


    }//GEN-LAST:event_btnXoaCauHoiActionPerformed

